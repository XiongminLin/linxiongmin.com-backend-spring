package linxiongmin.com;

public class EnglishVocab {
	private String year;
	private long value;
	
	public EnglishVocab(String year, long value) {
		this.year = year;
		this.value = value;
	}
	
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public long getValue() {
		return value;
	}
	public void setvalue(long value) {
		this.value = value;
	}

}
